package models;

public class almacenDTO {
    
    private String nombreAlmacen;
    private String areaAlmacen;
    private String tipoAlmacen;
    private String recaudoDia;
    private String cantidadVentas;
    private String recaudoMes;
    
    public String getNombreAlmacen() {
        return nombreAlmacen;
    }

    public void setNombreAlmacen(String nombreAlmacen) {
        this.nombreAlmacen = nombreAlmacen;
    }

    public String getAreaAlmacen() {
        return areaAlmacen;
    }

    public void setAreaAlmacen(String areaAlmacen) {
        this.areaAlmacen = areaAlmacen;
    }

    public String getTipoAlmacen() {
        return tipoAlmacen;
    }

    public void setTipoAlmacen(String tipoAlmacen) {
        this.tipoAlmacen = tipoAlmacen;
    }

    public String getRecaudoDia() {
        return recaudoDia;
    }

    public void setRecaudoDia(String recaudoDia) {
        this.recaudoDia = recaudoDia;
    }

    public String getCantidadVentas() {
        return cantidadVentas;
    }

    public void setCantidadVentas(String cantidadVentas) {
        this.cantidadVentas = cantidadVentas;
    }

    public String getRecaudoMes() {
        return recaudoMes;
    }

    public void setRecaudoMes(String recaudoMes) {
        this.recaudoMes = recaudoMes;
    }
    
    
    

    

    
}